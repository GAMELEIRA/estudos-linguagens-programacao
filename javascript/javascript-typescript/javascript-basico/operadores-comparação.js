/**
 * Operadores de comparação
 * > maior que 
 * >= maior ou igual
 * < menor 
 * <= menor ou igual
 * == igualdade
 * === extritamente igual
 * != diferente
 * !== extritamente diferente
 */

const num1 = 10;
const num2 = 20;

console.log(num1 > num2);
console.log(num1 >= num2);
console.log(num1 < num2);
console.log(num1 <= num2);
console.log(num1 == num2);
console.log(num1 === num2);
console.log(num1 != num2);
console.log(num1 !== num2);